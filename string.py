str1='keshav'
str2="prakash"
str3="""kolekar"""
print(str1)
print(str2)
print(str3)

# this all are valid ways to define string
# | Syntax                                         | Use Case                                                                           | Example              |
# | ---------------------------------------------- | ---------------------------------------------------------------------------------- | -------------------- |
# | `'single quotes'`                              | Simple strings, commonly used                                                      | `'hello'`            |
# | `"double quotes"`                              | Also for simple strings, especially helpful when the string contains single quotes | `"It's fine"`        |
# | `'''triple quotes'''` or `"""triple quotes"""` | For multi-line strings or docstrings                                               | `"""Line1\nLine2"""` |
