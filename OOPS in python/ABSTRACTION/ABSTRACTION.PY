from abc import ABC, abstractmethod

# ğŸ”· Abstract class
'''ABC: Stands for Abstract Base Class.

abstractmethod: Marks a method that must be implemented by any subclass.'''
class Animal(ABC):

    # ğŸ”’ Abstract method (must be defined by child)
    @abstractmethod      #tells Python: â€œThis method is compulsory for all subclasses.â€ (Every animal must have a sound().
                                         #But each animal makes a different sound.)
    def sound(self):     #means the method has no body here.
        pass

# ğŸ”¶ Concrete class
class Dog(Animal):
    def sound(self):
        print("Dog barks")

class Cat(Animal):
    def sound(self):
        print("Cat meows")




#uses the class 
d = Dog()
d.sound()   # Output: Dog barks

c = Cat()
c.sound()   # Output: Cat meows

