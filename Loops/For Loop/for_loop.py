# Example 1: range(5)
# Starts from 0 (default), goes up to 4 (5 is not included)
for i in range(5):
    print(i)  # Output: 0, 1, 2, 3, 4

print("-----")  # Separator for clarity

# Example 2: range(1, 5)
# Starts from 1, goes up to 4 (5 is not included)
for i in range(1, 5):
    print(i)  # Output: 1, 2, 3, 4

print("-----")  # Separator for clarity

# Example 3: range(1, 10, 2)
# Starts from 1, goes up to 9 (10 not included), with a step of 2
for i in range(1, 10, 2):
    print(i)  # Output: 1, 3, 5, 7, 9
